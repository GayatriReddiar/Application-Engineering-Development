    private void unregisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unregisterButtonActionPerformed
int selectedRow = registrationStatusJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select row from table!!!");
            return;
        }

        StudentApprovalWorkRequest approvalRequest = (StudentApprovalWorkRequest)registrationStatusJTable.getValueAt(selectedRow, 0);
        EducationEventWorkRequest ngoEvtReq = approvalRequest.getEventRequest();
        
        if(!(approvalRequest.getStatus().equals("Event Cancelled"))){
        ngoEvtReq.getStudentList().remove(userAccount.getEmp());
        if(ngoEvtReq.getStudentWaitList().size() > 0)
        {
            Employee waitlistedEmp = ngoEvtReq.getStudentWaitList().get(0);
            ngoEvtReq.getStudentList().add(waitlistedEmp);
            for (Network network : ecosystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if(organization instanceof StudentOrganization){
                        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
                            if(request.getSender().getEmp() == waitlistedEmp){
                                
                                request.setStatus("Registered");
                                
                                }
                            }
                        }
                    }
                }
            }
        }
        
        approvalRequest.setStatus("Unregistered");
        populateRegistrationsTable();
        JOptionPane.showMessageDialog(null, "Unregistration successful for workshop!");
        }
        else{
            approvalRequest.setStatus("Unregistered");
            JOptionPane.showMessageDialog(null, "Unregistration successful for cancelled event!");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_unregisterButtonActionPerformed

