    public void populateEventRequestTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestTable.getModel();
        
        model.setRowCount(0);
        for (Network network : ecosystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if(organization instanceof StudentOrganization){
                        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
                            if(request instanceof EducationEventWorkRequest){
                                EducationEventWorkRequest req = (EducationEventWorkRequest) request;                   
                                Object[] row = new Object[2];
                                row[0] = req;
                                row[1] = req.getNgoName();
                                model.addRow(row);    
                            }
                        }
                    }
                }
            }
        }
    }
     
    public void populateRegistrationsTable(){
        DefaultTableModel model = (DefaultTableModel)registrationStatusJTable.getModel();        
        model.setRowCount(0);
        for (Network network : ecosystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if(organization instanceof StudentCommunityOrganization){
                        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
                            if(request.getSender() == userAccount){
                                StudentApprovalWorkRequest req = (StudentApprovalWorkRequest) request;
                                Object[] row = new Object[4];
                                row[0] = req;
                                row[1] = req.getEventRequest();
                                row[2] = req.getEventRequest().getNgoName();
                                row[3] = req.getStatus();    
                                model.addRow(row);
                            }
                        }
                    }
                }
            }
            
        }
    }
    
