    public void registerEnabled() {
        for (Network network : ecosystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if(org instanceof StudentCommunityOrganization){
                        for(WorkRequest r : org.getWorkQueue().getWorkRequestList()){
                                StudentApprovalWorkRequest managerReq = (StudentApprovalWorkRequest) r;
                                if (request == managerReq.getEventRequest() && managerReq.getSender() == account && (managerReq.getStatus().equals("Event Cancelled") || managerReq.getStatus().equals("Pending with Club") || managerReq.getStatus().equals("Registered"))){
                                    registerButton.setEnabled(false);
                                    break;
                                } 
                                else {
                                    registerButton.setEnabled(true);
                                }
                        }
                    }
                }
            }
        }
    }
    
