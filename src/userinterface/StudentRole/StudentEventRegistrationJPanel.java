    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String date = dateTextField.getText();
        StudentApprovalWorkRequest studentRequest = new StudentApprovalWorkRequest() ;
        Organization org = null;
        studentRequest.setDate(date);
        studentRequest.setSender(account);
        studentRequest.setStatus("Pending with Club");
        studentRequest.setEventRequest(request);
        for(Network network : ecosystem.getNetworkList()){
            for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
                    if(o instanceof StudentCommunityOrganization){
                        studentRequest.setReceiver(o.getUserAccountDirectory().getUserAccount(account.getEmp().getManagerID()));
                        org = o;
                    }
                }
            }
        }        
        if(org!=null){
        org.getWorkQueue().getWorkRequestList().add(studentRequest);
        account.getQueue().getWorkRequestList().add(studentRequest);
        }
        JOptionPane.showMessageDialog(null, "Registration Approval Request Sent!!!");
        registerButton.setEnabled(false);
    }//GEN-LAST:event_registerButtonActionPerformed


    
