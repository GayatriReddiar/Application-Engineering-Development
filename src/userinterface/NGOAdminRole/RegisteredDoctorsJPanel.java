    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)doctorsTable.getModel();   
        dtm.setRowCount(0);
        for(Employee employee : request.getDoctorList()){
            Object[] row = new Object[2];
            row[0] = employee;
            dtm.addRow(row);                
        }        
    }

