    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (Network network : ecoSystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if(org instanceof HeadDoctorOrganization){
                        for(WorkRequest request : org.getWorkQueue().getWorkRequestList()){
                            if(request instanceof HeadDoctorApprovalWorkRequest & (request.getStatus().equals("Pending with HoD"))){
                                HeadDoctorApprovalWorkRequest req = (HeadDoctorApprovalWorkRequest)request;
                                if(request.getReceiver() == account){
                                    Object[] row = new Object[3];
                       
                                    row[0] = req;
                                    row[1] = req.getWorkRequest().getDate();
                                    row[2] = req.getWorkRequest().getDuration();
   
                                    model.addRow(row);
                                }
                            }
                        }
                    }
                }
            }
        }
    }

