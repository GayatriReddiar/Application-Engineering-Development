    private void approvedJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approvedJButtonActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select row from table");
            return;
        }

        HeadDoctorApprovalWorkRequest request = (HeadDoctorApprovalWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        HealthEventWorkRequest ngoEventReq = request.getWorkRequest();
        if(ngoEventReq.getDoctorList().size() < Integer.parseInt(ngoEventReq.getDoctorCount())){
            request.setStatus("Registered");
            UserAccount senderOfrequest = request.getSender();
            Employee senderEmp = senderOfrequest.getEmp();
            ngoEventReq.getDoctorList().add(senderEmp);
            senderOfrequest.getQueue().getWorkRequestList().add(ngoEventReq);;
        }
        else{
            request.setStatus("Waitlisted");
            UserAccount senderOfrequest = request.getSender();
            Employee senderEmp = senderOfrequest.getEmp();
            ngoEventReq.getDoctorWaitList().add(senderEmp);
        }
        
        JOptionPane.showMessageDialog(null, "Approved!");
        
        populateTable();
        // TODO add your handling code here:
    }//GEN-LAST:event_approvedJButtonActionPerformed


