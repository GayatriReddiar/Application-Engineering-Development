public void populateOrdersTable(){
        DefaultTableModel model = (DefaultTableModel) pharmacyJTable.getModel();
        
        model.setRowCount(0);
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if(organization instanceof PharmacyOrganization){
                        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
                            if(request instanceof PrescriptionOrderWorkRequest){
                                if(request.getStatus().equals("Sent to Pharmacy")){                                  
                                PrescriptionOrderWorkRequest req = (PrescriptionOrderWorkRequest) request;
                                Object[] row = new Object[2];
                    //            row[0] = request;
                    //            row[1] = request.getSender().getEmployee().getName();
                    //            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
                    //            row[3] = request.getStatus();
                                row[0] = req;
                                row[1] = req.getChildName();

                                model.addRow(row);
                                }
                            }
                        }
                    }
                }
            }
        }
    }
