    private void viewDetailsjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = pharmacyJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select row from table");
            return;
        }
        PrescriptionOrderWorkRequest request = (PrescriptionOrderWorkRequest)pharmacyJTable.getValueAt(selectedRow, 0);
        PrescriptionOrderDetailsJPanel prescriptionOrderDetailsJPanel = new PrescriptionOrderDetailsJPanel(userProcessContainer, request, userAccount, organization, business);
        userProcessContainer.add("PrescriptionOrderDetailsJPanel", prescriptionOrderDetailsJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewDetailsjButtonActionPerformed


